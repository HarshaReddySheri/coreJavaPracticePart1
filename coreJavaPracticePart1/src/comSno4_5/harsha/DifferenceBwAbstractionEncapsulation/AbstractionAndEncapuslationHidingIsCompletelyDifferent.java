package comSno4_5.harsha.DifferenceBwAbstractionEncapsulation;

public class AbstractionAndEncapuslationHidingIsCompletelyDifferent {
	/*
	 * Encapsulation -> Hiding the data (not giving direct data) through the methods
	 * *************
	 * Only data hiding & we can't see any implementation in the method
	 * 
	 * Abstraction -> Hiding the implementation details showing only functionality to the user
	 * ***********
	 * Entire implementation you are hiding 
	 */
}
